aa=[[1, 2, 3, 4],
    [4, 5, 6, 7],
    [8, 9,10,11]
    ]
print("aa    = %s" %aa)
print("aa[0] = %s" %aa[0])
print("aa[1] = %s" %aa[1])
print("aa[2] = %s" %aa[2])

print("aa[0:2] = %s" %aa[0:2])          # aa[0] , aa[1]
print("aa[2][1:3] = %s" %aa[2][1:3])    # 9,10

print("aa[0:2][1:3] = %s" %aa[0:2][1:3]) # aa[1]
